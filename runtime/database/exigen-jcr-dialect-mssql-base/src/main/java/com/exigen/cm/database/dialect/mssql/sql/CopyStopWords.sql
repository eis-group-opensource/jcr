 @DataFilePath varchar(250) AS 
DECLARE @STMT nvarchar(1000)
BEGIN
 IF EXISTS (SELECT * FROM sysobjects WHERE name='%%STOPWORD-TMP%%')
 BEGIN
  DROP TABLE %%USER-SCHEMA%%.%%STOPWORD-TMP%%
 END
 CREATE TABLE %%USER-SCHEMA%%.%%STOPWORD-TMP%%  (%%STOPWORD-COL%% varchar(1000))
 SET @STMT='BULK INSERT %%USER-SCHEMA%%.%%STOPWORD-TMP%% FROM '''+@DataFilePath+''''
