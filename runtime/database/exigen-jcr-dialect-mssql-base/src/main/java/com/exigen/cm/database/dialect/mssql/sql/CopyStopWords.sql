   WHERE %%STOPWORD-COL%% IS NOT NULL AND
    %%STOPWORD-COL%% NOT IN (SELECT %%STOPWORD-COL%% from %%USER-SCHEMA%%.%%STOPWORD-TAB%%)
 DROP TABLE %%USER-SCHEMA%%.%%STOPWORD-TMP%%
END

